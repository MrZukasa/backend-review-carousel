import { PrismaClient } from "@prisma/client";

const prisma = new PrismaClient();

async function main() {
  const allGames = [
    ...gamesA, ...gamesB,
    ...gamesC, ...gamesD,
    ...gamesE, ...gamesF,
    ...gamesG, ...gamesH,
    ...gamesI, ...gamesK,
    ...gamesL, ...gamesM,
    ...gamesO, ...gamesP,
    ...gamesR, ...gamesS,
    ...gamesT, ...gamesW, ...gamesY
  ];

  for (const game of allGames) {
    await prisma.game.create({
      data: {
        nomeGioco: game.name,
        votoLancio: game.launchScore,
        votoAggiornato: game.updatedScore,
        recensioneOriginale: game.originalReview,
        analisiAggiornata: game.updatedAnalysis,
        ultimaRevisione: game.lastReviewDate,
      },
    });
  }

  console.log("Seed completato!");
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
